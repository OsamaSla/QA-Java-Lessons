package Operators;

public class Operators {

	public static void main(String[] args) {
		long num = 0;
		num = 20 + 30;
		System.out.println(num);

		num = 30 * 3 + 5;
		System.out.println(num);

		num = (10 + 5) * 3;
		System.out.println(num);

		num = num + 10;
		System.out.println(num);

		num++; // ---> num = num +1;
		System.out.println(num);

		num--; // ---> num = num -1;
		System.out.println(num);

		num = 10;
		num *= 7; // ---> num = num * 7;

		num = 10 % 3;
		System.out.println(num);

		long a = 10;
		long b = 20;
		long c = a + b;
		System.out.println(c);

	}

}
