// While Loop 1-99 - הדפסת מספרים מ-1 עד 99 באמצעות לולאת while
package Loops;

public class Loops2 {
	public static void main(String[] args) {
        int i = 1; // Initialize counter
        // Loop as long as i is less than or equal to 99
        while (i <= 99) {
            System.out.println(i); // Print the current number
            i++; // Increment the counter
        }
    }
}
