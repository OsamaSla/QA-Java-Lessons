package VariablesOperators;

public class Ex7 {
	public static void main(String[] args) {
		double r = 10;
		double PI = 22/7;
		double H = 10;
		double W = 7;
		System.out.println("Circle Area is - " + PI*r*r);
		System.out.println("Rectangle Area is - " + H*W);
		System.out.println("Total Area is - " + (PI*r*r + H*W));
}}
